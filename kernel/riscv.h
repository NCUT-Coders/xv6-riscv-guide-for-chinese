/*
  xv6采用RISC-v分页中较为主流的sv39模式
  每次寻找页表项(PTE, page-table entry)时利用底部的39位而得名（余25位未用）
  
  sv39的部分细节内容：
    一个RISC-V 页表在逻辑上是一个2^27页表项(PageTable Entry, PTE)的数组。
    每次搜索页表项时，利用39位中的的高27位于页表中找到相关的PTE
    单个PTE占64位，目前由以下部分组成：
      *高10位暂未使用（保留扩展）；
      *低10位保留各种符号标志位；
      *中间44位保存物理页号(Physical Page Number，PPN)
    
    PPN将与39位中余下12位组合，形成 44(PPN)+12(offset)=56位的物理地址
    
  另：实际上，sv39分页总共采用三级分页进行：每个页表为4KB（即512个PTE)
      每级搜索利用27位中的9位定位相应PTE，最后才得出真正的物理地址
*/

/*
  satp(Supervisor Address Translation and Protection)寄存器：于S模式控制分页系统
      内核将使用satp中的根页表物理地址开始使用页表
      每个CPU都有自己的satp寄存器，方便各个CPU运行不同进程
*/

