/*
  xv6文件系统包含5个层次：
    **block（块）
      ===用于提高读写效率===
      由若干个扇区组成（linux中一般为8个），作为批量读取、分配数据的最小单位
    **log（日志）
      ===保证数据更新的原子性，避免崩溃，及时恢复===
      记载一系列对块的读写分配等操作；
      只有等到一个log完成并提交时，包含的各个操作才允许生效（类似Git的commit）
    **files（文件）
      ===相关数据的整合与抽象===
      由inode实现，包含了数据的位置、大小等信息
      使得数据有意义的基本单位：一切有意义的数据源组合都视为文件（包括设备与目录）
    **directories（目录）
      ===inode的指引===
      包含特殊信息（一个inode的列表），可借此找到其他inode
      （注意：目录本身也是一个文件/inode，只不过较为特殊）
    **names（命名）
      ===对用户层面的可见与抽象化===
      由路径以及文件名称组成（如/usr/rtm/xv6/fs.c），便于索引
*/

